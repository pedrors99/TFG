def hash(x):
    """
    Convierte un elemento de entrada en otro de salida.

    :param x: Valor de entrada.
    :return: Elemento obtenido a partir del elemento de entrada.
    """
    return x


def concat(x, y):
    """
    Crea un número entero obtenido de la concatenación de otros dos números enteros.

    :param x: Número que va a estar primero (a la izquierda) en la concatenación.
    :param y: Número que va a estar segundo (a la derecha) en la concatenación.
    :return: Entero con la concatenación de las entradas.
    """
    return int(str(x) + str(y))